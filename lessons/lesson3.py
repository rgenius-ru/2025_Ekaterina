# Срезы
text = 'Создайте программу, которая запрашивает у пользователя два числа'

print(text[0:3])  # [start : stop)
print(text[1:6])
print(type(text[1:6]))
print(type(text[1]))

print(text[1:6:2])  # [start : stop : step]
print(text[0:-1:3]) 

print(text[-1::-1])

print(text[0:])
print(text[:7])

